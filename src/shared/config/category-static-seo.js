/**
 * @param {string} [categoryId]
 * @returns {CategoryStaticSeo | null}
 */
export function getStaticCategorySeo(categoryId) {
  if (!categoryId) return null;
  const id = String(categoryId).trim();
  if (CATEGORY_STATIC_SEO[id]) return CATEGORY_STATIC_SEO[id];
  const lower = id.toLowerCase();
  for (const key of Object.keys(CATEGORY_STATIC_SEO)) {
    if (key.toLowerCase() === lower) return CATEGORY_STATIC_SEO[key];
  }
  return null;
}
